      <h1 class="alt">Q&A with <strong>Dr. </strong></h1>
      <p>According to the National STROKE Association&#0153; (NSA), stroke is the fourth leading cause of death in America and&nbsp;a leading&nbsp;cause of adult disability. However, &ldquo;up to 80% of strokes are preventable.&rdquo;  Dr. Rolando , M.D., shares 8 key ways to prevent stroke.      </p>
      <h3>OMH: How can we prevent a stroke from occurring?</h3>
      <p><u><strong>Dr.  </strong></u>: There are really 8 primary factors to stroke prevention. Diet is critical, but in fact all of these factors are related. People with high-fat, high-salt diets usually have an increased cardiovascular risk because these people tend to have high LDL cholesterol and high blood pressure. Often we suggest Mediterranean style diets that are high in vegetables and fruits. We want you to stay away from the &ldquo;whites&rdquo; – white rice, white pastas, white potatoes (yes, that includes French Fries) and all white breads and baked goods made with white flour. So, the 8 factors are:</p>
      <ol>
        <li>1) Improve diet</li>
        <li>2) Quit smoking </li>
        <li>3) Lower blood pressure</li>
        <li>4) Lower LDL/raise HDL </li>
        <li>5) Increase physical activity</li>
        <li>6) Maintain healthy weight </li>
        <li>7) Control diabetes </li>
        <li>8) Limit alcohol       </li>
      </ol>
      <h3>&nbsp;</h3>
      <h3>OMH: How is smoking a risk factor for stroke?</h3>
      <p><u><strong>Dr. </strong></u>: Studies have shown smoking raises your blood pressure, and we know that blood pressure is an important cog in stroke risk. Smoking also drops down your good HDL cholesterol and brings up your bad LDL cholesterol. Smoking has a cumulative effect – the more you smoke, the higher your risk.
      <h3>OMH: How does blood pressure play a role?</h3>
      <p><u><strong>Dr. </strong></u>: We know that 60 percent of strokes are attributed to hypertension. Your risk of stroke starts increasing when your systolic blood pressure goes over 120, and your diastolic goes over 85. If you look at national studies, less than 50% of the population has adequate blood pressure control. </p>
      <h3>OMH: Do you suggest people monitor their own blood pressure?</h3>
      <p><u><strong>Dr. </strong></u>: Nowadays you can get an appropriate blood pressure cuff for around $40 from your local drugstore – so I would say yes, that&rsquo;s a very good thing to do. </p>
      <h3>OMH: How much exercise is enough?</h3>
      <p><u><strong>Dr. </strong></u>: At least 5 days per week of 30 minutes moderate cardiovascular exercise. If you don&rsquo;t exercise at all, see your doctor first and make sure you can gradually work up to 30 minutes a day.</p>
      <h3>OMH: You mention alcohol consumption – doesn&rsquo;t the Mediterranean Diet allow a glass of red wine?</h3>
      <p><u><strong>Dr. </strong></u>: Yes, one glass of red wine increases your HDL level, which is a cardio-protector, so that&rsquo;s appropriate. However, heavy alcohol consumption, which includes beer by the way, is very bad.</p>
      <h3>OMH: What about low dose aspirin therapy for prevention of stroke and heart disease? </h3>
      <p><u><strong>Dr. </strong></u>: From the standpoint of cardiovascular risk, I am for it. Baby aspirin (81 mg.) is critical for men and women over the age of 40 – unless you have a history of bleeding, recent surgery, or an allergy to aspirin. I take it myself, and I started over 10 years ago.</p>
      <hr size="1" noshade="noshade" />
      <h3>&nbsp;</h3>
      <h3>According to <a href="http://www.mayoclinic.com/health/daily-aspirin-therapy/HB00073" target="_blank">MayoClinic.com</a>: </h3>
      <p>&ldquo;Aspirin interferes with your blood's clotting action. When you bleed, your blood's clotting cells, called platelets, build up at the site of your wound. The platelets help form a plug that seals the opening in your blood vessel to stop bleeding. But this clotting can also happen within the vessels that supply your heart with blood. If your blood vessels are already narrowed from atherosclerosis — the buildup of fatty deposits in your arteries — a fatty deposit in your vessel can cause the lining to burst. Then, a blood clot can quickly form and block the artery. This prevents blood flow to the heart and causes a heart attack. Aspirin therapy reduces the clumping action of platelets — possibly preventing a heart attack.&rdquo;</p>
      <p>The Mayo Clinic states &ldquo;You shouldn't start daily aspirin therapy on your own&rdquo; but should talk to your doctor about beginning aspirin therapy, especially if: </p>
      <p>• You've already had a heart attack or stroke. </p>
      <p>• You haven't had a heart attack, but you have had a stent placed in a coronary artery, you have had coronary bypass surgery, or you have chest pain due to coronary artery disease. </p>
      <p>• You've never had a heart attack, but you're at high risk of having one. </p>
      <p>• You're a man with diabetes who is older than 50, or a woman with diabetes who is older than 60.</p>
