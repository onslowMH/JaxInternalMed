      <h1 class="alt">Q&A with Dr. J </h1>
      <p>According to the Centers for Disease Control and Prevention (CDC), the current generation of children and teenagers has an obesity rate of 17%, three times higher than just one generation ago. Sadly, North Carolina is among the states with the highest rates of childhood obesity. As September is National Childhood Obesity Awareness Month, Dr. J. took the time to talk with us about this important issue.</p>
      <h3>OMH: How can we prevent childhood obesity from turning into adult diseases?</h3>
      <p><u><strong>Dr. J </strong></u>: Start with diet and exercise. Get kids away from the television and the video games! Childhood obesity is a national problem we've already seen translated into health problems in adults, especially cardiovascular disease. When we see obese young people, they have what we call metabolic syndrome - high blood pressure, high cholesterol, Type 2 diabetes or pre-diabetes.</p>
      <p>What can parents do right now? Say &quot;no&quot; to all soda (even diet soda). The CDC recommends ... water! Buy fresh fruits and vegetables for snacks - and don't dip vegetables in high calorie dressings or cover fruit with sugar and fat (i.e. whipped cream). Instead, mix veggies with healthy varieties of nut butters or hummus, and enjoy fruit with its natural sweetness.</p>
      <h3>OMH: How much exercise do you recommend?</h3>
      <p><u><strong>Dr. J</strong></u>: We recommend an equivalent of a 35-minute brisk walk, 5 times a week. That doesn't mean you have to walk, though. Whether it's running, bicycling, swimming, whatever you most like to do, that's okay as long as it's equivalent to the amount of effort you would put into 35 minutes of walking fast.
      <p>Think outside the treadmill. Exercise for kids and teens can include dancing, basketball, jumping rope, swimming, playing soccer, or bicycling. Studies show that physically active kids and teens have lower stress levels, increased self-esteem, and are more likely to perform well in school.
      <h3>OMH: What type of diet do you suggest?</h3>
      <p><u><strong>Dr. J </strong></u>: In my opinion, what works best is portion control, a low-carb and low-salt diet. Eating a good number of vegetables and fresh fruit is important. We always recommend that people eat a high fiber diet. I think if young people would reduce the amounts of starch and fat in their diets, they would be much better off later on in life. Sadly, many families eat at fast food restaurants because the cost is low.</p>
      <p>The initial low cost of eating fast food adds up to tremendous health and financial costs later in life, according to the CDC and National Institutes of Health. In addition, many schools contribute to the problem by selling soft drinks, candy, and fried foods (typically not cooked in healthy monounsaturated oils, such as olive oil and canola oil).        </p>
      <p>Simply stated, experts tell us childhood obesity is the result of too much high calorie food and not enough sustained physical exercise. By encouraging and participating in a life-long commitment to exercise and healthy eating, parents can help their children avoid the serious health consequences that too often result from childhood obesity. </p>
