                    <h3 style="color:#e7901f;">PCMH Care Coordinator</h3><br>
                    <h3>How does a Care Coordinator play a role in your health care as our patient?</h3>
                    The Care Coordinator works in collaboration and continuous partnership with patients and their family/caregiver(s), clinic/hospital/specialty providers and staff, and community resources to:

                    <ul>
                        <li>Improve overall patient satisfaction</li>
                        <li>Improve quality of care</li>
                        <li>Improve clinical outcomes and reduce costs</li>
                        <li>Provide quicker access to appropriate care</li>
                        <li>Reduce readmissions</li>
                        <li>Reduce unnecessary emergency department visits and hospitalizations</li>
                        <li>Reduce inpatient and overall charges</li>
                        <li>Significantly decrease medication costs</li>
                        <li>Increase use of preventative care measures</li>
                        <li>Create and promote a unique care plan that’s developed in coordination with the patient primary care provider, and family/caregiver(s)</li>
                        <li>Improve comprehension through patient-friendly communication and education efforts</li>
                        <li>Increase opportunity for patients’ self-management &amp; shared decision-making</li>
                        <li>Improve access to relevant community resources</li>
                        <li>Increase continuity of care by managing relationships with tertiary care providers, transition-in-care, and referrals</li>
                        <li>Provide medication reconciliation</li>
                    </ul>

