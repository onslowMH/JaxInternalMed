                    <h3 style="color:#e7901f;">PCMH Health Coach</h3><br>
                    <h3>What is a Health Coach?</h3>A Health Coach helps patients gain the knowledge, skills, tools and confidence to become active participants in their care so that they can reach their self-identified health goals. Health coaching encompasses five principal roles:
                    
                    <ul>
                        <li>Providing self-management support</li>
                        <li>Bridging the gap between clinician and patient</li>
